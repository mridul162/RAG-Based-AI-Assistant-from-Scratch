"""
chunk_loader.py

Purpose:
--------
Load chunk artifacts generated by the
ingestion pipeline.

Responsibilities:
-----------------
- Discover chunk artifact files
- Load chunk JSON data
- Return chunk records for embedding

Architecture Philosophy:
------------------------
Simple artifact consumption.
No transformation.
No business logic.
"""

import json

from pathlib import Path

from api.core.logging import (
    get_logger
)

logger = get_logger(__name__)


class ChunkArtifactLoader:

    def __init__(
        self,
        chunk_root: str,
    ):
        self.chunk_root = Path(
            chunk_root
        )

    def load(self) -> list[dict]:

        chunk_records = []

        chunk_files = sorted(

            self.chunk_root.rglob(
                "*.json"
            )
        )

        logger.info(
            f"Found {len(chunk_files)} "
            f"chunk artifact files."
        )

        for file_path in chunk_files:

            with open(
                file_path,
                "r",
                encoding="utf-8"
            ) as f:

                chunks = json.load(f)

            chunk_records.extend(
                chunks
            )

        logger.info(
            f"Loaded "
            f"{len(chunk_records)} "
            f"chunks."
        )

        return chunk_records
    

